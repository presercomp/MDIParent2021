    private void setCenterWindow(javax.swing.JInternalFrame e){
        this.childSize = e.getSize();
        e.setLocation((this.desktopSize.width - this.childSize.width)/2, (this.desktopSize.height - (this.childSize.height + 100))/2);
    }
